package com.example.remote_assitant.utilities;

public class Resolution {

    private long mWidth;
    private long mHeigth;

    public Resolution(long width, long heigth) {
        mWidth = width;
        mHeigth = heigth;
    }

    public long getWidth() {
        return mWidth;
    }

    public long getHeigth() {
        return mHeigth;
    }

}
